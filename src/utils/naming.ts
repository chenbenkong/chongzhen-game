/**
 * 根据"名"自动生成"字"（强制 2 字，且**与名不撞字**，符合古礼"字以表德"原则）
 *
 * 古代字法有 5 类（均要求字与名不同字）：
 *   ① 同义近义 —— 张衡字平子（衡=平）
 *   ② 互补相对 —— 孟轲字子舆
 *   ③ 相关延伸 —— 苏轼字子瞻（轼=车前横木，瞻=望）
 *   ④ 生肖对应 —— 唐寅字伯虎（寅=虎）
 *   ⑤ 同源同族 —— 杜甫字子美
 *
 * 算法：基于 nameSemanticMap 找与名语义相关的字，避开所有名字里有的字，按"前缀法"/"后缀法"/"双字法"随机
 *
 * @param givenName 名（1-2 字）
 * @returns 2 字字串（保证不与 givenName 撞字）
 */
export function generateCourtesyName(givenName: string): string {
  const name = givenName.trim()
  if (!name) return ''

  // 禁止字集：名里出现过的所有字
  const forbidden = new Set(name.split(''))

  // 1. 收集与名语义相关的字（先按语义，再过滤掉 forbidden）
  const relatedRaw: string[] = []
  for (const ch of name) {
    if (nameSemanticMap[ch]) relatedRaw.push(...nameSemanticMap[ch])
  }
  const relatedChars = [...new Set(relatedRaw)].filter(c => !forbidden.has(c))

  // 2. 兜底池：通用好字，过滤掉 forbidden
  const fallbackChars = generalGoodChars.filter(c => !forbidden.has(c))
  if (fallbackChars.length < 1) return ''

  // 3. 选字模式（仿古人字法）
  //    A. 前缀法（伯/仲/叔/季/子/公/君/元 + 1 字）—— 最常见
  //    B. 后缀法（1 字 + 甫/父/之/卿）—— 春秋战国最盛
  //    C. 双字自由组合 —— 取自相关字或通用字
  const mode = Math.random()
  const pickMid = (exclude?: string): string => {
    // 优先从 relatedChars 取，无则从 fallback 取
    const pool = (relatedChars.length > 0 ? relatedChars : fallbackChars)
      .filter(c => c !== exclude)
    if (pool.length === 0) return fallbackChars.filter(c => c !== exclude)[0] || ''
    return pool[Math.floor(Math.random() * pool.length)]
  }

  if (mode < 0.45) {
    // A. 前缀法
    const safePrefixes = courtesyPrefixes.filter(c => !forbidden.has(c))
    if (safePrefixes.length === 0) return pickMid() + pickMid('')  // 退化
    const prefix = safePrefixes[Math.floor(Math.random() * safePrefixes.length)]
    return prefix + pickMid(prefix)
  } else if (mode < 0.85) {
    // B. 后缀法
    const safeSuffixes = courtesySuffixes.filter(c => !forbidden.has(c))
    if (safeSuffixes.length === 0) return pickMid() + pickMid('')
    const suffix = safeSuffixes[Math.floor(Math.random() * safeSuffixes.length)]
    return pickMid(suffix) + suffix
  } else {
    // C. 双字自由
    const c1 = pickMid()
    let c2 = pickMid(c1)
    // 兜底再选一次（防 c2 === c1）
    if (c2 === c1) c2 = pickMid(c1 + 'x')  // 传个不会撞的 exclude
    return c1 + c2
  }
}

