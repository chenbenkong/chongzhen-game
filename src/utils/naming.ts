/**
 * 按科举身份推荐默认起始年龄
 * - 进士：22 岁（正常 22-25 中进士）
 * - 举人：24 岁（更老成）
 * - 秀才/童生：16-20
 */
export function defaultAgeByDegree(deg: string): number {
  switch (deg) {
    case '进士': return 22
    case '举人': return 24
    case '贡士': return 20
    case '秀才': return 18
    case '童生': return 16
    default: return 22
  }
}
